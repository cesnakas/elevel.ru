<p>
	 Щиты освещения (ЩО) выполняют функцию приема и распределения электроэнергии в осветительных сетях жилых и нежилых объектов и предназначены для осуществления приема и распределения электрической энергии в сетях с напряжением 380/220В с частотой 50Гц, нечастых оперативных включений и выключений электрических цепей и защиты сетей от перегрузок и токов короткого замыкания.
</p>
<p>
	 Щитки (щиты) осветительные применяются в осветительных и силовых установках промышленных объектов, жилых и административных зданий, производственных предприятий. Щиты освещения обеспечивают защиту потребительских линий от токов короткого замыкания и перегрузок и позволяют быстро и легко подключать и отключать большие группы энергопотребителей.
</p>
