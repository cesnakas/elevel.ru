<p>
 <b>Особенности конструкции, технические характеристики</b>
</p>
<p>
	 Осветительные щиты представляют собой конструкции из гнутых металлических профилей, внутри которых на монтажных панелях расположено необходимое оборудование. Корпус изготавливается из листовой стали и оснащается запирающейся на ключ дверцей, защищающей от несанкционированного доступа к коммутационным аппаратам.
</p>
<p>
</p>
<p>
	 Ввод и вывод питающих линий осуществляется в нижней и верхней части корпуса. В зависимости от условий эксплуатации корпус может иметь различную степень защиты (IP44 и IP54). Габариты готового щита (щитка) освещения зависят от количества и мощности подключаемых линий и используемых элементов автоматики.
</p>