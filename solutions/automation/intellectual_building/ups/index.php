   </strong></center><center style="text-align: left; "><strong>Генераторы</strong> (<strong>мини электростанции</strong>) - это собственные, независимые источники электроэнергии. Они широко используются во многих областях повседневной жизни при отсутствии или перебоях электроснабжения. В современных условиях, при повсеместном применении электричества и, практически полной от него зависимости всех систем жизнеобеспечения любых объектов (жилые дома, офисы, государственные учреждения, промышленные предприятия, гостиницы, спортивные сооружения и т.п.), трудно переоценить достоинства систем бесперебойного энергоснабжения, основой которых являются генераторные установки (мини электростанции).</center> 
<br />
 
<p>Генераторы, входящие в проект, сделанный исключительно для Вас компанией Элевел, это аппараты ведущих мировых производителей, отвечающие всем параметрам безопасности и обладающие показательной надежностью. Все это в совокупности с высокотехнологичным оборудованием, обязательным сервисным обслуживанием и грамотным подходом ведущих специалистов - обеспечит комфортное использование привычных бытовых приборов даже вдали от цивилизации.</p>
 
<br />
 
<p>Самый правильный поступок будущего владельца <strong>автономной электростанции</strong> (<strong>ДГУ</strong>) — это обращение в инжиниринговую компанию - то есть к нам. Наши специалисты начнут строительство мини электростанции с разработки проекта, а закончат сдачей её в эксплуатацию, с последующим сервисом, обучением персонала и поставкой расходных материалов. Кроме того, следует добавить, что мы, в отличие от официальных дилеров не связаны обязательствами с какой либо маркой, брендом или типом электростанции. Подбор генератора и вспомогательного оборудования ведется непредвзято и оптимально, с учетом всех потребностей. Бесперебойное электроснабжение объекта, главная цель использования генераторов (мини электростанций), но достигнуть ее без помощи систем автоматизации – невозможно. Современные системы автоматики в постоянном режиме отслеживают напряжение в основной сети, и как только происходит авария, автоматически запускают дизельный генератор (<strong>ДГУ</strong>, <strong>мини электростанцию</strong>). После запуска, уже через непродолжительное время, напряжение автоматически подается в обесточенный объект, а как только ток основной сети появляется и становится стабильным, обеспечение электричеством объекта переключается обратно на основную сеть.</p>
 
<br />
 
<p>Помимо этого, современные системы автоматики, могут в постоянном режиме мониторить состояние генератора (мини электростанции):</p>
 
